#include "../inc/cub3d.h"

int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

int ft_str_isdigit(char *s)
{
	if (!s || !*s)
		return (0);
	while (s && *s && ft_isdigit(*s))
		s++;
	return (!*s);
}